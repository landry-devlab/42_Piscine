int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	n;

	i = 0;
	n = 1;
	while (str[i] != '\0')
	{
		if (!('a' <= str[i] && str[i] <= 'z'))
			n = 0;
		i++;
	}
	return (n);
}
