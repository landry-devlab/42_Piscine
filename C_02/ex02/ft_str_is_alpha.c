int	ft_str_is_alpha(char *str)
{
	int	i;
	int	n;

	i = 0;
	n = 1;
	while (str[i] != '\0')
	{
		if (!('A' <= str[i] && str[i] <= 'Z'))
			if (!('a' <= str[i] && str[i] <= 'z' ))
				n = 0;
		i++;
	}
	return (n);
}
